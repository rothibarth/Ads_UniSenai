package modelo;

public class Acai {

	private String nomeCliente;

	private String tamanhoAcai;

	private int qtdeAcompanhamento;

	private String acompanhamentos;

	public String getTamanhoAcai() {
		return tamanhoAcai;
	}

	public void setTamanhoAcai(String tamanhoAcai) {
		this.tamanhoAcai = tamanhoAcai;
	}

	public String getAcompanhamentos() {
		return acompanhamentos;
	}

	public void setAcompanhamentos(String acompanhamentos) {
		this.acompanhamentos = acompanhamentos;
	}

	public int getQtdeAcompanhamento() {
		return qtdeAcompanhamento;
	}

	public void setQtdeAcompanhamento(int qtdeAcompanhamento) {
		this.qtdeAcompanhamento = qtdeAcompanhamento;
	}

	public String getNomeCliente() {
		return nomeCliente;
	}

	public void setNomeCliente(String nomeCliente) {
		this.nomeCliente = nomeCliente;
	}

}
